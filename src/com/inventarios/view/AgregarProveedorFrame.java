    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
 if (txtId.getText().trim().isEmpty() || txtNombre.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String id = txtId.getText().trim();
        String nombre = txtNombre.getText().trim();

        try {
            Proveedor proveedor = new Proveedor(id, nombre);
            controller.agregarProveedor(proveedor);
            JOptionPane.showMessageDialog(this, "Proveedor agregado exitosamente.");
            gestionProveedoresFrame.actualizarTablaProveedores();
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    
  

    
